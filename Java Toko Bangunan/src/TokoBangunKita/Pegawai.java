package TokoBangunKita;

/**
 *
 * @author acer
 */
public class Pegawai {
    private String kdPegawai;
    private String namaPegawai;
    private String posisiPegawai;
    private int noTelponPegawai;
    private int gajiPegawai;
    private String Username;
    private String Password;
    /**
     * @return the kdPegawai
     */
    public String getKdPegawai() {
        return kdPegawai;
    }

    /**
     * @param kdPegawai the kdPegawai to set
     */
    public void setKdPegawai(String kdPegawai) {
        this.kdPegawai = kdPegawai;
    }

    /**
     * @return the namaPegawai
     */
    public String getNamaPegawai() {
        return namaPegawai;
    }

    /**
     * @param namaPegawai the namaPegawai to set
     */
    public void setNamaPegawai(String namaPegawai) {
        this.namaPegawai = namaPegawai;
    }

    /**
     * @return the posisiPegawai
     */
    public String getPosisiPegawai() {
        return posisiPegawai;
    }

    /**
     * @param posisiPegawai the posisiPegawai to set
     */
    public void setPosisiPegawai(String posisiPegawai) {
        this.posisiPegawai = posisiPegawai;
    }

    /**
     * @return the noTelponPegawai
     */
    public int getNoTelponPegawai() {
        return noTelponPegawai;
    }

    /**
     * @param noTelponPegawai the noTelponPegawai to set
     */
    public void setNoTelponPegawai(int noTelponPegawai) {
        this.noTelponPegawai = noTelponPegawai;
    }

    /**
     * @return the gajiPegawai
     */
    public int getGajiPegawai() {
        return gajiPegawai;
    }

    /**
     * @param gajiPegawai the gajiPegawai to set
     */
    public void setGajiPegawai(int gajiPegawai) {
        this.gajiPegawai = gajiPegawai;
    }

    /**
     * @return the Username
     */
    public String getUsername() {
        return Username;
    }

    /**
     * @param Username the Username to set
     */
    public void setUsername(String Username) {
        this.Username = Username;
    }

    /**
     * @return the Password
     */
    public String getPassword() {
        return Password;
    }

    /**
     * @param Password the Password to set
     */
    public void setPassword(String Password) {
        this.Password = Password;
    }
}