package TokoBangunKita;

/**
 *
 * @author acer
 */
public class Pelanggan {
    private String kdPelanggan;
    private String  namaPelanggan;
    private int noTelponPelanggan;
    private String alamatPelanggan;
    /**
     * @return the kdPelanggan
     */
    public String getKdPelanggan() {
        return kdPelanggan;
    }

    /**
     * @param kdPelanggan the kdPelanggan to set
     */
    public void setKdPelanggan(String kdPelanggan) {
        this.kdPelanggan = kdPelanggan;
    }

    /**
     * @return the namaPelanggan
     */
    public String getNamaPelanggan() {
        return namaPelanggan;
    }

    /**
     * @param namaPelanggan the namaPelanggan to set
     */
    public void setNamaPelanggan(String namaPelanggan) {
        this.namaPelanggan = namaPelanggan;
    }

    /**
     * @return the noTelponPelanggan
     */
    public int getNoTelponPelanggan() {
        return noTelponPelanggan;
    }

    /**
     * @param noTelponPelanggan the noTelponPelanggan to set
     */
    public void setNoTelponPelanggan(int noTelponPelanggan) {
        this.noTelponPelanggan = noTelponPelanggan;
    }

    /**
     * @return the alamatPelanggan
     */
    public String getAlamatPelanggan() {
        return alamatPelanggan;
    }

    /**
     * @param alamatPelanggan the alamatPelanggan to set
     */
    public void setAlamatPelanggan(String alamatPelanggan) {
        this.alamatPelanggan = alamatPelanggan;
    }
}