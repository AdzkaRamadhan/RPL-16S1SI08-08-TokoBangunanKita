public class Barang {
    private String kdBarang;
    private String namaBarang;
    private int stokBarang;
    private int hargaJual;
    private int hargaBeli;
    /**
     * @return the kdBarang
     */
    public String getKdBarang() {
        return kdBarang;
    }

    /**
     * @param kdBarang the kdBarang to set
     */
    public void setKdBarang(String kdBarang) {
        this.kdBarang = kdBarang;
    }

    /**
     * @return the namaBarang
     */
    public String getNamaBarang() {
        return namaBarang;
    }

    /**
     * @param namaBarang the namaBarang to set
     */
    public void setNamaBarang(String namaBarang) {
        this.namaBarang = namaBarang;
    }

    /**
     * @return the stokBarang
     */
    public int getStokBarang() {
        return stokBarang;
    }

    /**
     * @param stokBarang the stokBarang to set
     */
    public void setStokBarang(int stokBarang) {
        this.stokBarang = stokBarang;
    }

    /**
     * @return the hargaJual
     */
    public int getHargaJual() {
        return hargaJual;
    }

    /**
     * @param hargaJual the hargaJual to set
     */
    public void setHargaJual(int hargaJual) {
        this.hargaJual = hargaJual;
    }

    /**
     * @return the hargaBeli
     */
    public int getHargaBeli() {
        return hargaBeli;
    }

    /**
     * @param hargaBeli the hargaBeli to set
     */
    public void setHargaBeli(int hargaBeli) {
        this.hargaBeli = hargaBeli;
    }
    
}
