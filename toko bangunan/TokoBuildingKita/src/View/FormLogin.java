    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
      try {
            sql = "SELECT * FROM KARYAWAN WHERE user_karyawan = '" + txtUsername.getText() + "' AND pass_karyawan = '" + txtPassword.getText()+ "'";
            st = con.con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                id_user = rs.getString("user_karyawan");

                hak = rs.getString("hak_akses");
            }
        } catch (SQLException ex) {
        }

        if (id_user != null) {

            this.dispose();
            if (hak.equals("Admin")) {
                JOptionPane.showMessageDialog(this, "Login Berhasil");
                FormUtama p = new FormUtama();
                p.setVisible(true);
                p.setMenuLoginAdmin();
                
            } else {
                JOptionPane.showMessageDialog(this, "Login Berhasil");
                FormUtama p = new FormUtama();
                p.setVisible(true);
                p.setMenuLoginKaryawan();
            }


        } else {
            JOptionPane.showMessageDialog(this, "Login Gagal, Coba Lagi!!");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

